// Varsayılan dil
let currentLanguage = localStorage.getItem('preferredLanguage') || 'de';

// Dili dəyişdirmə funksiyası
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    
    // HTML lang atributunu dəyişdir
    document.documentElement.lang = lang;
    
    // Bütün data-translate elementlərini yenilə
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Placeholder və aria-label kimi atributları yenilə
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    document.querySelectorAll('[data-translate-aria]').forEach(element => {
        const key = element.getAttribute('data-translate-aria');
        if (translations[lang] && translations[lang][key]) {
            element.setAttribute('aria-label', translations[lang][key]);
        }
    });
    
    // Aktiv dil düyməsini vurğula
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    document.querySelector(`[data-lang="${lang}"]`)?.classList.add('active');
}

// Səhifə yüklənəndə dili tətbiq et
document.addEventListener('DOMContentLoaded', function() {
    changeLanguage(currentLanguage);
});